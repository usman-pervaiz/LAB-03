# finding the distance covered by car

r = 0.2  # radius is denoted by r
w = 12.56   # angular velocity is denoted by w
v = r*w    # linear velocity is denoted by v
print("\n\nThe linear velocity of car is",v,"m/s")

t = 10   # Time is denoted by t
d = v*t  # distance is denoted by d
print("\nThe distance covered by the car is",d,"m")
      
