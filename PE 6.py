# finding the distance covered by the car

u = 50  # u denotes initial velocity
a = 10  # a denotes acceleration
t = 2   # t denotes time
v = u+(a*t)  # v donates linear velocity
print("\n\nThe linear velocity is",v,"m/s")

d = v*t  # d donates distance
print("\nThe distance covered by the car is",d,"m")

